# Datos
numeroA = 65
numeroB = 105
# Verificar cual es mayor y menor
isNumeroAmayor = numeroA > numeroB
print ("-"*10, "多A es mayor que B?", "-"*10)
print (isNumeroAmayor)
isNumeroAmenor = numeroA < numeroB
print ("-"*10, "多A es menor que B?", "-"*10)
print (isNumeroAmenor)
# Preguntar si son iguales
isIgual = numeroA == numeroB
print ("-"*10, "多A es igual a B?", "-"*10)
print (isIgual)
# Preguntar si son diferentes
isDiferente = numeroA != numeroB
print ("-"*10, "多A y B son diferentes?", "-"*10)
print (isDiferente)
# Sumar valores
sumar = numeroA + numeroB
print ("-"*10, "Cual es el resultado de sumar A y B?")
print (f"el resultado de la suma es {sumar}")
# Restar valores
restar = numeroA - numeroB
print ("-"*10, "Cual es el resultado de la resta entre A y B?", "-"*10)
print (f"el resultado de la resta es {restar}")
# Multiplicacion
multiplicar = numeroA * numeroB
print ("-"*10, "Cuanto es el resultado de multiplicar A y B?", "-"*10)
print (f"la respuesta es {multiplicar}")
# Division 
dividir = numeroB / numeroA
print ("-"*10, "Cual es el resultado de dividir B entre A?", "-"*10)
print (f"el resultado de la division es {dividir}")
# Potenciacion 
Potencia = numeroB ** numeroA
print ("-"*10, "Cuanto es B elevado a la A?", "-"*10)
print (f"el resultado es {Potencia}")
